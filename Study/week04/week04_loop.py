# 실습 1 - 기본적 for 문
hap = 0

for i in range(1, 11, 1):
    hap+= i
    
print(hap)

# 실습 2 - 50, 100까지 3 단위로 합산한 값 출력
result = 0

for i in range(50, 100, 3):
    result += i
print(result)


